class Maths:
    def __init__(self, a, b):
        self.a = a
        self.b = b
    def add_nums(self):
        return (self.a + self.b)
    def substract_nums(self):
        return (self.a - self.b)
    def multiply_nums(self):
        return (self.a * self.b)
    def divide_nums(self):
        return (self.a / self.b)
        
